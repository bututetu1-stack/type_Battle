// テーマ専用語（艦これ・四字熟語など）も「すべて」で出題されるようにする。
export const ALL_POOL: WordEntry[] = (() => {
  const seen = new Set<string>();
  const out: WordEntry[] = [];
  for (const e of [...WORD_POOL, ...Object.values(THEME_POOLS).flat()]) {
    const key = e.display + '|' + e.reading;
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(e);
  }
  return out;
})();
