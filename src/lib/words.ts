// 読み（かな）をトークン（判定単位）に分割する。
export const tokenizeWord = (reading: string): Token[] => {
  const tokens: Token[] = [];
  let i = 0;
  while (i < reading.length) {
    const char = reading[i];
    const nextChar = reading[i + 1] || '';
    if (ROMAJI_MAP[char + nextChar]) {
      tokens.push({ kana: char + nextChar, romaji: ROMAJI_MAP[char + nextChar] });
      i += 2;
    } else if (ROMAJI_MAP[char]) {
      tokens.push({ kana: char, romaji: ROMAJI_MAP[char] });
      i += 1;
    } else {
      tokens.push({ kana: char, romaji: [char] });
      i += 1;
    }
  }
  // 促音「っ」は次の音の頭の子音を重ねる表記をデフォルトにする（例: きって→t）。
  // xtu/ltu などの明示入力も引き続き許容する。
  tokens.forEach((tk, idx) => {
    if (tk.kana !== 'っ') return;
    const next = tokens[idx + 1];
    const c = next?.romaji[0]?.[0];
    if (c && !'aiueon'.includes(c)) {
      tk.romaji = [c, 'xtu', 'xtsu', 'ltu', 'ltsu'];
    }
  });
  return tokens;
};

// --- 振り仮名（ルビ）セグメント生成 ---
// 漢字部分にだけ振り仮名を付けるため、display を「漢字ラン / かな・記号ラン」に分け、
// reading を手がかりに各漢字ランの読みを切り出す。
export interface RubySeg {
  text: string;
  rt?: string; // 漢字ランのみ読みを持つ
}

// CJK 漢字（＋反復記号など）か判定する。
const isKanjiChar = (ch: string): boolean => {
  const c = ch.codePointAt(0) ?? 0;
  return (
    (c >= 0x4e00 && c <= 0x9fff) || // CJK統合漢字
    (c >= 0x3400 && c <= 0x4dbf) || // 拡張A
    ch === '々' ||
    ch === '〆' ||
    ch === 'ヶ' ||
    ch === 'ヵ'
  );
};

// カタカナ→ひらがな（reading[ひらがな]と照合するため）。
const kataToHira = (s: string): string =>
  s.replace(/[ァ-ヶ]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0x60));

// display と reading から、漢字部分にだけ rt（振り仮名）を付けたセグメント列を返す。
// ひらがな・カタカナ・記号には rt を付けない。
export function buildRuby(display: string, reading: string): RubySeg[] {
  // 同種文字（漢字 / 非漢字）が続く範囲をランにまとめる。
  const runs: { text: string; kanji: boolean }[] = [];
  for (const ch of display) {
    const kanji = isKanjiChar(ch);
    const last = runs[runs.length - 1];
    if (last && last.kanji === kanji) last.text += ch;
    else runs.push({ text: ch, kanji });
  }

  const hasKanji = runs.some((r) => r.kanji);
  if (!hasKanji) {
    // 漢字なし。表示にラテン文字など「日本語の仮名以外」が含まれ、かつ読みと違うなら
    // 語全体に読みをルビとして乗せる（海外艦名 Bismarck/Ark Royal などを原語表示）。
    const isKana = (c: string) => {
      const o = c.codePointAt(0) ?? 0;
      return (o >= 0x3040 && o <= 0x309f) || (o >= 0x30a0 && o <= 0x30ff);
    };
    const foreign = [...display].some((c) => !isKana(c) && !/[\sー・]/.test(c));
    if (foreign && reading && reading !== display) return [{ text: display, rt: reading }];
    // それ以外（かなのみ）は振り仮名不要。
    return runs.map((r) => ({ text: r.text }));
  }

  const segs: RubySeg[] = [];
  let ri = 0; // reading 内のポインタ
  let consistent = true; // display のかなが reading 内に順番どおり現れるか
  for (let i = 0; i < runs.length; i++) {
    const run = runs[i];
    if (!run.kanji) {
      // かな/記号ランは reading 上をそのまま進める（カタカナはひらがな化して照合）。
      const hira = kataToHira(run.text);
      if (reading.startsWith(hira, ri)) ri += hira.length;
      else consistent = false; // 表示中のかなが読みに無い＝当て字的で対応づけ不能
      segs.push({ text: run.text });
    } else {
      // 次のかなランの位置を手がかりに、この漢字ランの読みを切り出す。
      const next = runs[i + 1];
      let furi: string;
      if (next) {
        const nextHira = kataToHira(next.text);
        const idx = reading.indexOf(nextHira, ri);
        if (idx >= 0) {
          furi = reading.slice(ri, idx);
          ri = idx;
        } else {
          // 次のかなランを読み中に見つけられない＝対応づけ不能。
          furi = reading.slice(ri);
          ri = reading.length;
          consistent = false;
        }
      } else {
        furi = reading.slice(ri);
        ri = reading.length;
      }
      segs.push({ text: run.text, rt: furi || undefined });
    }
  }

  // 「打ち止め＝ラストオーダー」のように、表示のかなが読みに対応しない当て字では
  // 上のアルゴリズムが先頭の漢字に読みを全振りしてしまう。その場合は単語全体を
  // ひとつのルビ単位にして、読みを語全体の上に乗せる（漫画のルビと同じ振る舞い）。
  if (!consistent) return [{ text: display, rt: reading }];
  return segs;
}

// 単語IDの連番カウンタ（React の key 用。シードに依存せず常に一意）。
let idCounter = 0;

function buildWord(entry: WordEntry, type: WordType, prefix: string): Word {
  return {
    id: `${prefix}${idCounter++}`,
    display: entry.display,
    reading: entry.reading,
    type,
    tokens: tokenizeWord(entry.reading),
  };
}

// 与えられた決定論的 RNG から新しい単語を生成する。
// theme を指定すると、そのテーマ（タグ）の語彙からのみ出題する。
// recent に直近出題した表示テキストを渡すと、それらを避けて選ぶ（連続/近接重複の防止）。
// localType=true のとき、種別（お宝/おじゃま）だけはクライアントごとにローカル乱数で決める。
//   → 出題する単語列（エントリ）はシード共有で全員同じだが、お宝の出現は各自に保証される
//     （オンラインでお宝＝アイテムが特定プレイヤーにしか出ない問題への対策）。
// テーマの複数選択をトグルする。'all' は排他（選ぶと他を解除）。
// 個別テーマを選ぶと 'all' を外す。空になったら 'all' に戻す。カンマ区切り文字列で返す。
export function toggleThemeSelection(current: string, id: string): string {
  if (id === 'all') return 'all';
  const set = new Set((current || '').split(',').map((s) => s.trim()).filter((s) => s && s !== 'all'));
  if (set.has(id)) set.delete(id);
  else set.add(id);
  const arr = [...set];
  return arr.length ? arr.join(',') : 'all';
}

// テーマ指定から出題プールを解決する。
// theme は単一ID（'all'/'food' 等）またはカンマ区切りの複数指定（'food,it'）。
// 'all' か未指定は全語彙（WORD_POOL＋全テーマプールをマージ）。
export function poolForThemes(theme: string = 'all'): WordEntry[] {
  const ids = (theme || 'all').split(',').map((s) => s.trim()).filter(Boolean);
  // 'all'／未指定は全語彙＋追加語句。順序を固定し、オンラインでも全員同じプール＝
  // シード同期が崩れないようにする。除外テーマ(EXCLUDED_THEMES)がある場合はそれを抜く。
  if (ids.length === 0 || ids.includes('all')) {
    if (EXCLUDED_THEMES.size === 0) return [...ALL_POOL, ...EXTRA_WORDS];
    // 除外テーマに属する語のキーを集める。テーマ語は WORD_POOL にも重複し得るため、
    // テーマのプールをスキップするだけでは消えない。キー単位で最終プールから除外する。
    const exKeys = new Set<string>();
    for (const tid of EXCLUDED_THEMES) {
      const p = THEME_POOLS[tid];
      if (p) for (const e of p) exKeys.add(e.display + '|' + e.reading);
    }
    const out: WordEntry[] = [];
    const seen = new Set<string>();
    const push = (e: WordEntry) => {
      const k = e.display + '|' + e.reading;
      if (seen.has(k) || exKeys.has(k)) return;
      seen.add(k); out.push(e);
    };
    WORD_POOL.forEach(push);
    for (const [tid, pool] of Object.entries(THEME_POOLS)) if (!EXCLUDED_THEMES.has(tid)) pool.forEach(push);
    EXTRA_WORDS.forEach(push);
    return out;
  }
  const merged: WordEntry[] = [];
  for (const id of ids) {
    if (id === 'custom') { merged.push(...EXTRA_WORDS); continue; }
    // 自作テーマ（'custom:テーマ名'）。そのグループの追加語句だけを出題する。
    if (id.startsWith('custom:')) {
      const g = id.slice('custom:'.length);
      merged.push(...EXTRA_WORDS.filter((w) => (w.group || '') === g));
      continue;
    }
    const p = THEME_POOLS[id];
    if (p) merged.push(...p);
  }
  return merged.length ? merged : [...ALL_POOL, ...EXTRA_WORDS];
}

// 'all'(全語彙)出題から除外するテーマ。例: 艦これ('kancolle')を除外。
// 全クライアントで同じにする必要がある（ソロは個人設定、オンラインは部屋設定）。
let EXCLUDED_THEMES = new Set<string>();
export function setExcludedThemes(ids: string[]): void {
  EXCLUDED_THEMES = new Set((ids || []).filter((x) => typeof x === 'string'));
}

// --- 追加語句（プレイヤーが追加したカスタム語句） ---
// ソロは端末の保存ぶん、オンラインは部屋の共有ぶんを、ゲーム開始前にここへ流し込む。
// 全クライアントで同じ並びにすることでシード同期（お題の順番）が一致する。
interface ExtraWord extends WordEntry { group?: string }
let EXTRA_WORDS: ExtraWord[] = [];
export function setExtraWords(list: { display: string; reading: string; group?: string }[]): void {
  EXTRA_WORDS = (list || [])
    .filter((w) => w && w.display && w.reading && isTypeableReading(w.reading))
    .map((w) => ({ display: w.display, reading: w.reading, ...(w.group ? { group: w.group } : {}) }));
}
export function getExtraWords(): WordEntry[] {
  return EXTRA_WORDS.slice();
}

// 読み（かな）が最後までローマ字入力できるか検証する（追加語句のバリデーション用）。
export function isTypeableReading(reading: string): boolean {
  if (!reading) return false;
  // 1文字ずつローマ字表に存在するか（漢字・カタカナ・記号など打てない文字を弾く）。
  // tokenizeWord は未知文字をそのまま通すため、ここで明示的に弾く必要がある。
  for (const c of reading) {
    if (!ROMAJI_MAP[c]) return false;
  }
  const toks = tokenizeWord(reading);
  if (toks.length === 0) return false;
  // トークンのかなを連結して元の読みと一致し、かつ全トークンに綴り候補があること。
  const joined = toks.map((t) => t.kana).join('');
  if (joined !== reading) return false;
  return toks.every((t) => t.romaji && t.romaji.length > 0 && t.romaji[0].length > 0);
}

// 出題バッグ（シャッフル袋）。プールの全語を1巡するまで同じ語を出さないための状態。
// items = まだ出していない語のインデックス列（末尾から取り出す）。last = 直近に出したindex。
export interface WordBag { items: number[]; last: number }
export const newWordBag = (): WordBag => ({ items: [], last: -1 });

export const generateWord = (
  rng: RNG,
  theme: string = 'all',
  bag: WordBag = newWordBag(),
  localType = false,
  treasureProb = 0.2,
  treasureBonus = 0, // 「お宝出現率アップ」アイテムなどの恒久ボーナス（加算）
): Word => {
  const pool = poolForThemes(theme);
  // バッグが空なら、プール全体をシャッフルして詰め直す（=新しい1巡を開始）。
  if (bag.items.length === 0) {
    for (let i = 0; i < pool.length; i++) bag.items.push(i);
    // Fisher-Yates（rng はシード列。オンラインでもクライアント間で同じ並びになる）。
    for (let i = bag.items.length - 1; i > 0; i--) {
      const j = Math.floor(rng() * (i + 1));
      const t = bag.items[i]; bag.items[i] = bag.items[j]; bag.items[j] = t;
    }
    // 巡の境目で「前巡の最後」と「次に出す語」が連続しないよう、必要なら入れ替える。
    if (pool.length > 1 && bag.items[bag.items.length - 1] === bag.last) {
      const t = bag.items[bag.items.length - 1];
      bag.items[bag.items.length - 1] = bag.items[0];
      bag.items[0] = t;
    }
  }
  let idx = bag.items.pop();
  if (idx === undefined || idx >= pool.length) idx = (idx ?? 0) % pool.length; // プール変化時の保険
  bag.last = idx;
  const entry = pool[idx];
  // 種別用の乱数は常にシード列を1つ消費する（エントリ列の同期を崩さない）。
  const seededRand = rng();
  const rand = localType ? Math.random() : seededRand;
  let type: WordType = 'normal';
  if (localType) {
    // オンライン: お宝の出現率はホストが 0〜100% で設定可能（treasureProb）。
    // 相手から飛んでくるおじゃまで割合が薄まるため、自動供給のおじゃまは少なめ（約8%）。
    const tp = Math.min(0.95, Math.max(0, treasureProb + treasureBonus));
    if (rand < tp) type = 'treasure';
    else if (rand < tp + 0.08) type = 'ojama';
  } else {
    // ソロ: お宝出現率はカスタム設定（treasureProb／＋ボーナス）/ おじゃま 約20%。
    const tp = Math.min(0.95, Math.max(0, treasureProb + treasureBonus));
    if (rand < tp) type = 'treasure';
    else if (rand < tp + 0.2) type = 'ojama';
  }
  return buildWord(entry, type, 'w');
};

// 受信したおじゃま用の単語（常に ojama）。攻撃由来なので Math.random で選ぶ。
// theme を渡すと、選択テーマの語彙からおじゃまを選ぶ（テーマと無関係な語が来ないように）。
export const makeOjamaWord = (theme: string = 'all'): Word => {
  const pool = poolForThemes(theme);
  const entry = pool[Math.floor(Math.random() * pool.length)];
  return buildWord(entry, 'ojama', 'o');
};

// 短い単語を生成（アイテム「短縮」でバックログを軽くするのに使う）。
// テーマ内の短い語を優先し、無ければ全体の短い語から選ぶ。
export const makeShortWord = (type: WordType = 'normal', theme: string = 'all'): Word => {
  const pool = poolForThemes(theme);
  const shorts = pool.filter((w) => w.reading.length >= 3 && w.reading.length <= 4);
  const src = shorts.length ? shorts : ALL_POOL.filter((w) => w.reading.length >= 3 && w.reading.length <= 4);
  const entry = src[Math.floor(Math.random() * src.length)];
  return buildWord(entry, type, 's');
};

